    id_curso INT(11) UNSIGNED NOT NULL,
    id_disciplina INT(11) UNSIGNED NOT NULL,
    id_modulo INT(11) UNSIGNED NOT NULL,
    ano_mod VARCHAR(50),
    ordem_mod INT UNSIGNED,
    hora_mod DECIMAL(2,1) UNSIGNED,
    hora_modFeitas DECIMAL(2,1) UNSIGNED,
